# Looping Statements
# Execute/Run statement(s) multiple times (N times).

# Loop Design
	# 1. Start Value
	# 2. Stop Value
	# 3. Condition
	# 4. Statement(s)
	# 5. Modifier

# Example
# decalre
start = None
stop = None
value = "Broadway"

# input
stop = input("Enter how many times : ")

# process
"""
start=1 # start
stop=5 # stop
start<=stop # condition
print(value) # statement
start+=1 # modifier
"""

start=1 # start
stop = int(stop)

while(start<=stop): # Condition
	print(value) # Statement(s)
	start+=1 # Modifier
print("Out")


